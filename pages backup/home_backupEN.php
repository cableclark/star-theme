            <h3>The red umbrella – global symbol of sex work</h3>
<!-- /wp:heading -->
<!-- wp:paragraph -->
            <p class="card__paragraph">The red umbrella symbolizes the resistance against violence, the weather and human assaults. </p>
            <div class="start hide">
                <p class="card__paragraph">
                It symbolizes the protection of sex workers from abuse by the police, pimps, clients, and society alike.The red color symbolizes love. </p>
                <p class="card__paragraph"> The red umbrella was first used by sex workers and their supporters in Venice, Italy in 2001. The so-called First World Congress of sex workers was an art installation created by the Slovenian artist Tadej Pogachar, exhibited within the frames of the 49th Venetian Biennial between the 6th and 8th June 2001.</p>
                <p class="card__paragraph">This was set up in a public area under a tent in the “Prostitutes’ Pavilion” where participants and supporters from Taiwan, Thailand, Vietnam, Italy, Cambodia, Germany, the USA and Austria gathered. They presented the situation in the market and discussed to develop a strategy to fight for sex workers human rights. Activists organized video projections, exhibitions, performances, street theater, and distribution of printed materials. </p>
                <p class="card__paragraph"> One of the activities included the Red Umbrella March. Sex workers marched on the streets of Venice using megaphones and red umbrellas in order to direct the attention to the bad working conditions and the violation of their human rights that they have to face on a daily basis. </p>
                <p class="card__paragraph ">The march began in front of the tent of the pavilion moving towards the center of the city. The International Committee for Sex Workers’ Rights adopted the red umbrella as a symbol of resistance against discrimination. Everyone is aware of the meaning that the red ribbon bears, therefore, the red umbrella could have the same significance.</p>
