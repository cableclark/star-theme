            <h3>The Sex Workers’ Declaration</h3>
            <!-- /wp:heading -->
            <!-- wp:paragraph -->
            <p class="card__paragraph">The Sex Workers’ Declaration was created by sex workers and organizations dedicated to promoting their human rights and wellbeing. </p>
            <div class="start hide">
                <p class="card__paragraph ">
                The declaration clearly states the list of human rights that every citizen in Europe, including sex workers have in accordance with the International Human Rights Law. </p>
                <p class="card__paragraph ">The Declaration states the measures and practices that all its signatories believe are necessary to ensure minimum respect and protection of these rights. These rights have to be respected throughout the process of introducing policies and programs created to deal with human trafficking, irregular migration and terrorism.</p>
